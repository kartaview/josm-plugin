package org.openstreetmap.josm.plugins.openstreetcam.entity;

/**
 * @author nicoleta.viregan
 */
public class PixelPoint {

    private final double x;
    private final double y;

    public PixelPoint(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}